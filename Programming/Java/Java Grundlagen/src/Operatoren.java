
public class Operatoren {

	public static void main(String[] args) {
		/*
		 * Mit Operatoren kann man Variablen ändern oder allgemein mit ihnen arbeiten
		 */
		
		// Zuweisungsoperator
		int lebenGegner = 100;
		int geschossEnergie = 20;
		
		lebenGegner -= geschossEnergie; // lebenGegner-geschossEnergie = lebenGegner
		System.out.println("Leben des Gegners: " + lebenGegner);
	}

}
