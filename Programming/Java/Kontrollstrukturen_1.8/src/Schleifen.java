public class Schleifen {

	public static void main(String[] args) {
		// For-Loops --> Schleifendurchgänge bekannt
		// While-Loops --> Schleifendurchgänge unbekannt
		 
		/*
		 * Aufbau For-Loop: 
		 * for (Anfangswert; Abbruchbedingung; Zähler){
		 * Anweisungsblock
		 * }
		 */
		for (int i = 0; i<10; i++){
			System.out.println(i);
		}
		
		for (int j=1; j<=10; j++) {
			if (j == 5) {
				System.out.println("Das ist eine 5");
			}
		}
		
		
		
		/*
		 * Aufbau While-Loop:
		 * while (){
		 * Anweisungsblock
		 * }
		 */
		
		
	}
}
