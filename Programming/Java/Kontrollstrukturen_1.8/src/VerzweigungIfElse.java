
public class VerzweigungIfElse {

	public static void main(String[] args) {
		int i = 10;
		if (i == 1) {
			System.out.println("Zahl ist: " + i);
		}else if (i >= 1) {
			System.out.println("Zahl i ist größer als 1");
		}else if (i <= 1) {
			System.out.println("Zahl i ist kleiner als 1");
		}else {
			System.out.println("Zahl ist unbekannt");
		}

	}

}
